#La suma de dos numeros
print ("OPERACIONES")
a = float(input("Ingrese el primer numero:   "))
b = float(input("Ingrese el segundo numero:   "))


print ("La suma es :  ",  a + b)
print ("La resta es :  ", a - b)
print ("La multi es :  ", a * b)
if b == 0:
    print ("Dato invalido")
    
else:    
    print ("La div es :   ",   a / b)
